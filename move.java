
public class move {

	int col; 
	int value;
	int win;
	
	public move(int col, int value) {
		this.col = col;
		this.value = value;
		this.win = win;
	}

	public int getCol() {
		return col;
	}

	public int getValue() {
		return value;
	}

	public void setCol(int col) {
		this.col = col;
	}

	public void setValue(int value) {
		this.value = value;
	}
	
	
}
